    private void btnAsistenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsistenciaActionPerformed
        
    int[] selectedIndices = lstLista.getSelectedIndices();

    for (int selectedIndex : selectedIndices) {
        Estudiante estudiante = listaEstudiantes.get(selectedIndex);
        estudiante.setAsistio(true);
    }

   
    actualizarListaEstudiantes();
    
    }//GEN-LAST:event_btnAsistenciaActionPerformed

    private void actualizarListaEstudiantes() {
    DefaultListModel<String> listModel = (DefaultListModel<String>)
            lstLista.getModel();
    listModel.clear();

    for (Estudiante estudiante : listaEstudiantes) {
        String estado = estudiante.isAsistio() ? "Asisti√≥" : "Ausente";
        listModel.addElement(estudiante.getNombre() + " - "
                + estudiante.getCurso() + " - " + estudiante.getTurno()
                + " - " + estado);
    }
}
